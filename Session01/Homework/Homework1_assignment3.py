# Assignment 3: Write a program that converts Celsius (C) into Fahrenheit (F)

celsius = int(input("Enter the temperature in Celsius:"))
fahrenheit = 9/5*celsius + 32
print("The temperature in Fahrenheit is", fahrenheit, "(F)")

